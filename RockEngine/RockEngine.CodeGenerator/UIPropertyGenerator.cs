using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp.Syntax;

using System;
using System.Collections.Generic;
using System.Collections.Immutable;
using System.Globalization;
using System.Linq;

namespace RockEngine.CodeGenerator
{
    [Generator]
    public class UIPropertyGenerator : IIncrementalGenerator
    {
        public void Initialize(IncrementalGeneratorInitializationContext context)
        {
            // Find all class declarations that might be components
            var classDeclarations = context.SyntaxProvider
                .CreateSyntaxProvider(
                    predicate: static (s, _) => IsSyntaxTargetForGeneration(s),
                    transform: static (ctx, _) => GetSemanticTargetForGeneration(ctx))
                .Where(static m => m is not null);

            // Combine the class declarations with the compilation
            var compilationAndClasses = context.CompilationProvider.Combine(classDeclarations.Collect());

            // Register the source generator
            context.RegisterSourceOutput(compilationAndClasses, static (spc, source) => Execute(source.Left, source.Right, spc));
        }

        private static bool IsSyntaxTargetForGeneration(SyntaxNode node)
        {
            return node is ClassDeclarationSyntax classDecl &&
                   classDecl.Modifiers.Any(m => m.IsKind(Microsoft.CodeAnalysis.CSharp.SyntaxKind.PublicKeyword) ||
                                               m.IsKind(Microsoft.CodeAnalysis.CSharp.SyntaxKind.InternalKeyword));
        }

        private static ClassDeclarationSyntax GetSemanticTargetForGeneration(GeneratorSyntaxContext context)
        {
            var classDeclaration = (ClassDeclarationSyntax)context.Node;

            // Check if the class implements IComponent or has Component attribute
            var semanticModel = context.SemanticModel;
            var classSymbol = semanticModel.GetDeclaredSymbol(classDeclaration);

            // Add type check and cast to INamedTypeSymbol
            if (classSymbol is INamedTypeSymbol namedTypeSymbol && IsComponentClass(namedTypeSymbol))
            {
                return classDeclaration;
            }

            return null;
        }

        private static bool IsComponentClass(INamedTypeSymbol classSymbol)
        {
            // Check if class implements IComponent or has Component attribute
            return (classSymbol.AllInterfaces.Any(i => i.Name == "IComponent") ||
                   classSymbol.GetAttributes().Any(a => a.AttributeClass?.Name == "ComponentAttribute")) && !classSymbol.IsAbstract;
        }

        private static void Execute(Compilation compilation, ImmutableArray<ClassDeclarationSyntax> classes, SourceProductionContext context)
        {
            if (classes.IsDefaultOrEmpty)
            {
                return;
            }

            foreach (var classDeclaration in classes)
            {
                var model = compilation.GetSemanticModel(classDeclaration.SyntaxTree);
                var classSymbol = model.GetDeclaredSymbol(classDeclaration) as INamedTypeSymbol;

                if (classSymbol != null && IsComponentClass(classSymbol))
                {
                    var source = GenerateUIPropertyAccessors(classSymbol, classDeclaration);
                    context.AddSource($"{classSymbol.Name}_UIProperties.g.cs", source);
                }
            }
        }

        private static string GenerateUIPropertyAccessors(INamedTypeSymbol classSymbol, ClassDeclarationSyntax classDecl)
        {
            var namespaceName = classSymbol.ContainingNamespace.ToDisplayString();
            var className = classSymbol.Name;
            var properties = GetUIProperties(classSymbol);

            // Используем только минимальные using-директивы
            var usingStatements = @"using System;
using System.Collections.Generic;
using RockEngine.Core.ECS.Components;
using RockEngine.Core.Helpers;
using RockEngine.Core.Attributes;";

            var source = $$"""
            // <auto-generated/>
            #nullable enable
            
            {{usingStatements}}

            namespace {{namespaceName}}
            {
                public partial class {{className}}
                {
                    private static readonly UIPropertyAccessor[] _uiPropertyAccessors = new UIPropertyAccessor[]
                    {
            {{GeneratePropertyAccessors(properties)}}
                    };

                    public static IReadOnlyList<UIPropertyAccessor> GetUIPropertyAccessors() => _uiPropertyAccessors;
                }

                public struct {{className}}PropertyAccess
                {
                    private readonly {{className}} _component;

                    public {{className}}PropertyAccess({{className}} component)
                    {
                        _component = component;
                    }
            {{GeneratePropertyAccessMethods(properties)}}
                }
            }
            """;

            return source;
        }

        private static string GeneratePropertyAccessors(IEnumerable<IPropertySymbol> properties)
        {
            var accessors = new List<string>();

            foreach (var property in properties)
            {
                var uiAttr = property.GetAttributes()
                    .FirstOrDefault(a => a.AttributeClass?.Name == "UIEditableAttribute");

                var displayName = uiAttr?.NamedArguments
                    .FirstOrDefault(kvp => kvp.Key == "DisplayName").Value.Value?.ToString() ?? property.Name;

                // Используем полное имя типа для typeof
                var propertyTypeForTypeOf = GetFullTypeNameForTypeOf(property.Type);

                // Используем полное имя типа для приведения
                var propertyTypeForCast = GetFullTypeName(property.Type);

                var accessorCode = $$"""
                            new UIPropertyAccessor(
                                name: "{{property.Name}}",
                                displayName: "{{displayName}}",
                                propertyType: typeof({{propertyTypeForTypeOf}}),
                                getValue: (component) => (({{property.ContainingType.Name}})component).{{property.Name}},
                                setValue: {{(property.SetMethod != null && property.SetMethod.DeclaredAccessibility == Accessibility.Public ?
                                        $"(component, value) => (({property.ContainingType.Name})component).{property.Name} = ({propertyTypeForCast})value" : "null")}},
                                canWrite: {{(property.SetMethod != null && property.SetMethod.DeclaredAccessibility == Accessibility.Public).ToString().ToLower()}},
                                attributes: new Attribute[] 
                                {
                                    {{GetAttributeInitializers(property)}}
                                }
                            )
            """;

                accessors.Add(accessorCode);
            }

            return string.Join(",\n", accessors);
        }

        private static string GetFullTypeNameForTypeOf(ITypeSymbol typeSymbol)
        {
            // Получаем полное имя типа для typeof, обрабатывая nullable
            var nonNullableType = typeSymbol.NullableAnnotation == NullableAnnotation.Annotated
                ? typeSymbol.WithNullableAnnotation(NullableAnnotation.None)
                : typeSymbol;

            if (nonNullableType is INamedTypeSymbol { IsGenericType: true } genericType &&
                genericType.OriginalDefinition.SpecialType == SpecialType.System_Nullable_T)
            {
                nonNullableType = genericType.TypeArguments[0];
                return $"{nonNullableType.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat)}?";
            }

            return typeSymbol.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat);
        }

        private static string GetFullTypeName(ITypeSymbol typeSymbol)
        {
            // Получаем полное имя типа для использования в коде
            return typeSymbol.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat);
        }

        private static string GeneratePropertyAccessMethods(IEnumerable<IPropertySymbol> properties)
        {
            var methods = new List<string>();

            foreach (var property in properties)
            {
                var typeName = GetFullTypeName(property.Type);
                methods.Add($$"""
                    public {{typeName}} Get{{property.Name}}() => _component.{{property.Name}};
            """);

                if (property.SetMethod != null && property.SetMethod.DeclaredAccessibility == Accessibility.Public)
                {
                    methods.Add($$"""
                    public void Set{{property.Name}}({{typeName}} value) => _component.{{property.Name}} = value;
                """);
                }
            }

            return string.Join("\n", methods);
        }

        private static string GetAttributeInitializers(IPropertySymbol property)
        {
            var attributes = property.GetAttributes()
                .Where(a => a.AttributeClass?.Name == "RangeAttribute" ||
                           a.AttributeClass?.Name == "ColorAttribute" ||
                           a.AttributeClass?.Name == "StepAttribute")
                .Select(attr =>
                {
                    if (attr.AttributeClass?.Name == "RangeAttribute")
                    {
                        var min = GetRangeValue(attr.ConstructorArguments[0]);
                        var max = GetRangeValue(attr.ConstructorArguments[1]);
                        return $"new global::RockEngine.Core.Attributes.RangeAttribute({min}, {max})";
                    }
                    else if (attr.AttributeClass?.Name == "ColorAttribute")
                    {
                        return "new global::RockEngine.Core.Attributes.ColorAttribute()";
                    }
                    else if (attr.AttributeClass?.Name == "StepAttribute")
                    {
                        var step = GetStepValue(attr.ConstructorArguments[0]);
                        return $"new global::RockEngine.Core.Attributes.StepAttribute({step})";
                    }
                    return null;
                })
                .Where(a => a != null);

            return string.Join(", ", attributes);
        }

        private static string GetStepValue(TypedConstant constant)
        {
            if (constant.Value == null)
            {
                return "0.1f";
            }

            if (constant.Type?.SpecialType == SpecialType.System_Single)
            {
                float floatValue = (float)constant.Value;
                return FormatFloatLiteral(floatValue);
            }

            return $"{constant.Value}f";
        }

        private static string GetRangeValue(TypedConstant constant)
        {
            if (constant.Value == null)
            {
                return "0f";
            }

            if (IsFloatMaxValue(constant))
            {
                return "float.MaxValue";
            }

            if (IsFloatMinValue(constant))
            {
                return "float.MinValue";
            }

            if (constant.Type?.SpecialType == SpecialType.System_Single)
            {
                float floatValue = (float)constant.Value;
                return FormatFloatLiteral(floatValue);
            }

            return $"{constant.Value}f";
        }

        private static bool IsFloatMaxValue(TypedConstant constant)
        {
            if (constant.Value is float floatValue)
            {
                return floatValue == float.MaxValue;
            }

            if (constant.Value is double doubleValue)
            {
                return doubleValue >= 3.4028234663852886e38;
            }

            return false;
        }

        private static bool IsFloatMinValue(TypedConstant constant)
        {
            if (constant.Value is float floatValue)
            {
                return floatValue == float.MinValue;
            }

            if (constant.Value is double doubleValue)
            {
                return doubleValue <= -3.4028234663852886e38;
            }

            return false;
        }

        private static string FormatFloatLiteral(float value)
        {
            var stringValue = value.ToString("G9", CultureInfo.InvariantCulture);
            if (!stringValue.EndsWith("f", StringComparison.OrdinalIgnoreCase))
            {
                stringValue += "f";
            }
            return stringValue;
        }

        private static IEnumerable<IPropertySymbol> GetUIProperties(INamedTypeSymbol classSymbol)
        {
            return classSymbol.GetMembers()
                .OfType<IPropertySymbol>()
                .Where(p => p.GetMethod != null &&
                           p.GetMethod.DeclaredAccessibility == Accessibility.Public &&
                           !p.IsStatic &&
                           !p.GetAttributes().Any(a => a.AttributeClass?.Name == "SerializeIgnoreAttribute"));
        }
    }
}